class Solution {
public:

    int MAX_ARR_LEN = 600 + 1;
    int MAX_STR_LEN = 100 + 1;
    Solution() {}

    int findMaxForm(vector<string>& strs, int m, int n) {

        // dp[i][j]: max number of strings with at most i zeros and j ones
        vector<vector<int>> dp(m + 1, vector<int>(n + 1, 0));

        for (const auto& s : strs) {
            int zeros = getZeroCnt(s);
            int ones = s.size() - zeros;
            LOG("Processing string: " + s << " zeros:" << zeros << " ones:" << ones);

            // Traverse backwards to avoid using the same string multiple times
            for (int i = m; i >= zeros; --i) {
                for (int j = n; j >= ones; --j) {
                    dp[i][j] = max(dp[i][j], dp[i - zeros][j - ones] + 1);
                }
            }

            LOG("\tCurrent DP table last row:" << dp[m]);
        }
        LOG("Returning result: " << dp[m][n]);
        return dp[m][n];
    }

    int getZeroCnt(const string& s) {
        int cnt = 0;
        for (auto c : s) {
            if (c == '0') cnt++;
        }
        return cnt;
    }

    OUTPUT_TYPE proxyFunction(TestCaseStruct& nums) {
        return findMaxForm(nums.mData1, nums.mData2, nums.mData3);
    }
};


